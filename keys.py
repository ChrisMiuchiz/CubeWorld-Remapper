def GetKeyCode(key):
    if key == '':#User didn't press a key
        return 0 #All of those are invalid or unknown.
                 #CW's keycode structure seems irregular.
    #Convert to caps
    key = key.upper()
    
    for i in range(0, len(CWKeycodes)):
        if CWKeycodes[i] == key:
            return i
        
    #Key not found
    return 0
    
